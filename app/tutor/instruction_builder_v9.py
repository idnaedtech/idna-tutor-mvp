# ─────────────────────────────────────────────
# BUILDER — the main function
# ─────────────────────────────────────────────

def build(
    action: str,
    session: Any,
    instruction: Dict[str, Any],
    already_tried: Optional[list] = None,
) -> Dict[str, Any]:
    """
    Build a complete LLM prompt from FSM handler instructions.

    Args:
        action: One of the ACTION_PROMPTS keys ("teach", "reteach", "hint", etc.)
        session: SessionState object with student context
        instruction: Dict of action-specific parameters from the handler
        already_tried: List of explanations/analogies already used this session

    Returns:
        Dict with "system" and "user" keys ready for the LLM API call.
    """

    # v9.0.10: Correction handler — intercept if student is correcting Didi's math
    if instruction.get("student_is_correcting"):
        student_text = instruction.get("student_text", "")
        lang_pref = getattr(session, 'preferred_language', 'hinglish')
        use_english = lang_pref == "english"

        system = DIDI_SYSTEM_PROMPT.format(
            language=lang_pref,
            max_sentences=2,
            student_name=getattr(session, 'student_name', 'Student'),
            board_name=getattr(session, 'board_name', 'CBSE'),
            class_level=getattr(session, 'class_level', 8),
            chapter_number=getattr(session, 'chapter_number', ''),
            chapter_name=getattr(session, 'chapter_name', ''),
            topic=instruction.get('topic', getattr(session, 'current_concept_id', 'mathematics')),
        )

        if use_english:
            user_prompt = f'Student corrected your math: "{student_text}". You MUST acknowledge: "You\'re right, thank you for catching that!" Then give the correct fact. Do NOT ignore the correction. 2 sentences max.'
        else:
            user_prompt = f'Student ne tumhari math correct ki: "{student_text}". Pehle maafi maango: "Haan, sahi kaha aapne, sorry!" Phir sahi answer do. 2 sentences max.'

        return {"system": system, "user": user_prompt}

    # --- Build system prompt with session context ---
    system = DIDI_SYSTEM_PROMPT.format(
        language=getattr(session, 'preferred_language', 'hindi'),
        max_sentences=_max_sentences_for_action(action),
        student_name=getattr(session, 'student_name', 'Student'),
        board_name=getattr(session, 'board_name', 'CBSE'),
        class_level=getattr(session, 'class_level', 8),
        chapter_number=getattr(session, 'chapter_number', ''),
        chapter_name=getattr(session, 'chapter_name', ''),
        topic=instruction.get('topic', getattr(session, 'current_concept_id', 'mathematics')),
    )

    # --- Build action prompt with instruction details ---
    action_template = ACTION_PROMPTS.get(action)
    if not action_template:
        # Unknown action — fall back to a safe generic prompt
        action_template = "TASK: Respond helpfully to the student about {topic}. Max 2 sentences."

    # Merge instruction dict with defaults for safe formatting
    format_vars = _build_format_vars(action, session, instruction, already_tried)

    try:
        user_prompt = action_template.format(**format_vars)
    except KeyError as e:
        # Missing variable — use template with whatever we have
        user_prompt = f"TASK: Continue the tutoring session about {format_vars.get('topic', 'the current topic')}. Max 2 sentences."

    return {
        "system": system,
        "user": user_prompt,
    }


def _max_sentences_for_action(action: str) -> int:
    """Return max sentences allowed for each action type."""
    limits = {
        "greet": 2,
        "teach": 3,
        "reteach": 2,
        "ask_question": 2,
        "hint": 2,
        "correct_feedback": 1,
        "wrong_feedback": 1,
        "comfort": 2,
        "meta_answer": 1,
        "wrap_up": 3,
        "redirect_troll": 1,
        "handle_garbled": 1,
        "language_switch_ack": 1,
    }
    return limits.get(action, 2)


def _build_format_vars(
    action: str,
    session: Any,
    instruction: Dict[str, Any],
    already_tried: Optional[list] = None,
) -> Dict[str, Any]:
    """
    Build a complete dict of format variables for the action template.
    Uses instruction values first, falls back to session, then defaults.
    """
    topic = instruction.get(
        'topic',
        getattr(session, 'current_concept_id', 'mathematics')
    )

    return {
        # Common
        "topic": topic,
        "material": instruction.get('material', ''),
        "language": getattr(session, 'preferred_language', 'hindi'),
        "student_name": getattr(session, 'student_name', 'Student'),
        "student_text": instruction.get('student_text', ''),

        # Teaching
        "layer": instruction.get('layer', 'L1'),
        "confusion_count": instruction.get(
            'confusion_count',
            getattr(session, 'reteach_count', 0)
        ),
        "already_tried": _format_already_tried(already_tried),

        # Questions
        "question_text": instruction.get('question_text', ''),
        "correct_answer": instruction.get('correct_answer', ''),
        "student_answer": instruction.get('student_answer', ''),
        "hint_level": instruction.get('hint_level', 1),

        # Meta
        "meta_info": instruction.get('meta_info', ''),

        # Wrap-up
        "questions_attempted": instruction.get(
            'questions_attempted',
            getattr(session, 'questions_attempted', 0)
        ),
        "questions_correct": instruction.get(
            'questions_correct',
            getattr(session, 'questions_correct', 0)
        ),
        "topics_covered": instruction.get(
            'topics_covered',
            getattr(session, 'topics_covered', '')
        ),

        # Language switch
        "target_language": instruction.get('target_language', 'english'),
        "brief_topic_context": instruction.get(
            'brief_topic_context',
            f"we were learning about {topic}"
        ),
    }


def _format_already_tried(already_tried: Optional[list]) -> str:
    """Format the list of already-tried explanations for the prompt."""
    if not already_tried:
        return "Nothing yet — this is the first explanation."
    formatted = []
    for i, attempt in enumerate(already_tried, 1):
        # Truncate long explanations to keep prompt lean
        text = str(attempt)[:150]
        formatted.append(f"{i}. {text}")
    return "\n".join(formatted)


