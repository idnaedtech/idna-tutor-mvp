# HANDLER INTEGRATION HELPERS
# ─────────────────────────────────────────────

def build_for_teaching(session, material_text: str, content_layer: str = "L1"):
    """Shortcut for TEACHING state — normal teaching."""
    return build(
        action="teach",
        session=session,
        instruction={
            "material": material_text,
            "layer": content_layer,
        },
    )


def build_for_reteach(session, material_text: str, already_tried: list):
    """Shortcut for TEACHING state — student said IDK."""
    confusion = getattr(session, 'reteach_count', 0) + 1

    # At confusion >= 4, switch to comfort action
    if confusion >= 4:
        return build(
            action="comfort",
            session=session,
            instruction={
                "student_text": "I don't understand (repeated confusion)",
            },
        )

    return build(
        action="reteach",
        session=session,
        instruction={
            "material": material_text,
            "confusion_count": confusion,
        },
        already_tried=already_tried,
    )


def build_for_hint(session, question: dict, hint_level: int, student_answer: str = ""):
    """Shortcut for HINT state."""
    return build(
        action="hint",
        session=session,
        instruction={
            "question_text": question.get("question_tts", ""),
            "correct_answer": question.get("answer", ""),
            "hint_level": hint_level,
            "student_answer": student_answer,
        },
    )


def build_for_greeting(session):
    """Shortcut for GREETING state."""
    return build(
        action="greet",
        session=session,
        instruction={},
    )


def build_for_correct(session, question: dict, student_answer: str):
    """Shortcut for correct answer feedback."""
    return build(
        action="correct_feedback",
        session=session,
        instruction={
            "question_text": question.get("question_tts", ""),
            "student_answer": student_answer,
        },
    )


def build_for_wrong(session, question: dict, student_answer: str):
    """Shortcut for wrong answer feedback."""
    return build(
        action="wrong_feedback",
        session=session,
        instruction={
            "question_text": question.get("question_tts", ""),
            "correct_answer": question.get("answer", ""),
            "student_answer": student_answer,
        },
    )


def build_for_meta(session, student_text: str, meta_info: str):
    """Shortcut for meta-question response."""
    return build(
        action="meta_answer",
        session=session,
        instruction={
            "student_text": student_text,
            "meta_info": meta_info,
        },
    )


def build_for_language_switch(session, target_language: str):
    """Shortcut for language switch acknowledgment."""
    return build(
        action="language_switch_ack",
        session=session,
        instruction={
            "target_language": target_language,
        },
    )


def build_for_wrap_up(session):
    """Shortcut for session end."""
    return build(
        action="wrap_up",
        session=session,
        instruction={},
    )


def build_for_comfort(session, student_text: str):
    """Shortcut for emotional support."""
    return build(
        action="comfort",
        session=session,
        instruction={
            "student_text": student_text,
        },
    )


